# String -> tipe data gabungan dari banyak karakter
# Integer -> tipe data bilangan bulat
# Float -> tipe data bilangan desimal
# Boolean -> Tipe data kondisi
# Kompleks -> tipe data gabungan bilangan real dan imajiner
"""List -> tipe data yang menyimpan kumpulan data yang memiliki tipe data yang
bervariasi serta isinya dapat berubah - ubah"""
"""Tuple -> tipe data yang menyimpan kumpulan data yang memiliki tipe data yang
bervariasi serta isinya tidak dapat berubah - ubah"""
"""Dictionary -> tipe data yang menyimpan kumpulan data yang memiliki tipe data yang
bervariasi berupa pasangan penunjuk dan nilai"""

# Tipe data String
nama_saya = "Budi Kusuma"
print(type(nama_saya))

# Tipe data Integer
umur_saya = 20
print(type(umur_saya))

# Tipe data Float
tinggi_saya = 170.5
print(type(tinggi_saya))

# Tipe data Boolean
saya_masih_sekolah = True
print(type(saya_masih_sekolah))

# Tipe data Kompleks
print(type(1 + 5j))

# Tipe data list
list = ["xyz", 786, 2.23]
print(type(list))

# Tipe data Tuple
tuple = ("xyz", 768, 2.23)
print(type(tuple))

# Tipe data dictionary
dictionary = {"nama": "Adi Putra", "npm": "123456", "Kelas": "4IA21", "IPK": "3.80"}
print(type(dictionary))
